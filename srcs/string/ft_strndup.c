#include "libft.h"

char	*ft_strndup(const char *src, size_t n)
{
	size_t		i;
	size_t		len;
	char		*dest;

	len = ft_strlen(src);
	if (len > n)
		len = n;
	dest = (char *)malloc((len + 1) * sizeof(char));
	if (dest == NULL)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
