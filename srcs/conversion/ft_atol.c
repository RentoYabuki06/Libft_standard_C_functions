#include "libft.h"

long	ft_atol(const char *s)
{
	long	num;
	int		sign;

	num = 0;
	sign = 1;
	while ((*s >= 9 && *s <= 13) || *s == ' ')
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			sign = -1;
		s++;
	}
	while (ft_isdigit(*s))
		num = num * 10 + (*s++ - '0');
	return (num * sign);
}
